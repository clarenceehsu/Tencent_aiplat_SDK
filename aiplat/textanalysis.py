import time

from aiplat.base import AiPlatBase


class textanalysis(AiPlatBase):

    def nlp_wordseg(self, text: str):

        self.url = self.url_prefix + 'nlp/nlp_wordseg'
        self.data = {
            'app_id': self.app_id,
            'app_key': self.app_key,
            'time_stamp': int(time.time()),
            'nonce_str': int(time.time()),
            'text': text,
        }
        self.data['sign'] = self.genSignString(self.data)
        result = self.invoke(self.data)
        if result['ret']:
            return result['msg']

        return result['data']

